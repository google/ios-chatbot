#import "UIColor+GILAdditions.h"

@implementation UIColor (GILAdditions)

+ (UIColor *)gil_colorWithARGB:(NSUInteger)argb {
  int blue = argb & 0xff;
  int green = argb >> 8 & 0xff;
  int red = argb >> 16 & 0xff;
  int alpha = argb >> 24 & 0xff;

  return [UIColor colorWithRed:red / 255.0f
                         green:green / 255.0f
                          blue:blue / 255.0f
                         alpha:alpha / 255.f];
}

@end
